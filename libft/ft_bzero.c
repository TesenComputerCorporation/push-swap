#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ss;

	if (!n)
		return ;
	i = 0;
	ss = (char *)s;
	while (i < n)
	{
		ss[i] = '\0';
		i++;
	}
}
