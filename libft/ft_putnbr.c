#include "libft.h"

void	ft_putnbr(int n)
{
	int k;
	int d;

	if (n == -2147483648)
	{
		write(1, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	d = n / 10;
	k = n % 10 + '0';
	if (n > 9)
		ft_putnbr(d);
	write(1, &k, 1);
}
